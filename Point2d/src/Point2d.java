package Lab2;
public class Point2d {
    private double x,y,z;
    public Point2d ( double xCoord, double yCoord) {
        x = xCoord;
        y = yCoord;
    }
    public double getX () {
        return x;
    }
    public double getY () {
        return y;
    }
    public void setX ( double xCoord) {
        x = xCoord;
    }
    public void setY ( double yCoord) {
        y = yCoord;
    }

}

